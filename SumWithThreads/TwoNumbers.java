package Q1;

public class TwoNumbers {

	int num1 = 0;
	int num2 = 0; 
	
	public TwoNumbers(int num1, int num2) {
		
		this.num1 = num1;
		this.num2 = num2;
		
	}

	public int getNum1() {
		return num1;
	}
	
	public int getNum2() {
		return num2;
	}

	public int addNumbers() {
		return num1+num2;
	}
}
